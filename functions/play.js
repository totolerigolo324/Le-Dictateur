const ytdl = require("erit-ytdl");
const scdl = require("soundcloud-downloader");
const { canModifyQueue, STAY_TIME } = require("../util/music");
const db = require("quick.db");

module.exports = {
  async play(song, message) {
    const { SOUNDCLOUD_CLIENT_ID } = require("../util/music");

    let config;

    try {
      config = require("../config");
    } catch (error) {
      config = null;
    }

    const PRUNING = config.PRUNING;

    const queue = message.client.queue.get(message.guild.id);

    if (!song) {
      setTimeout(function () {
        if (queue.connection.dispatcher && message.guild.me.voice.channel) return;
        queue.channel.leave();
        queue.textChannel.send("En train de quitter le canal vocal...");
      }, STAY_TIME * 1000);
      queue.textChannel.send("‚ùå La file d'attente est termin√©e.").catch(console.error);
      return message.client.queue.delete(message.guild.id);
    }

    let stream = null;
    let streamType = song.url.includes("youtube.com") ? "opus" : "ogg/opus";

    try {
      if (song.url.includes("youtube.com")) {
        stream = await ytdl(song.url, { highWaterMark: 1 << 25 });
      } else if (song.url.includes("soundcloud.com")) {
        try {
          stream = await scdl.downloadFormat(song.url, scdl.FORMATS.OPUS, SOUNDCLOUD_CLIENT_ID);
        } catch (error) {
          stream = await scdl.downloadFormat(song.url, scdl.FORMATS.MP3, SOUNDCLOUD_CLIENT_ID);
          streamType = "unknown";
        }
      }
    } catch (error) {
      if (queue) {
        queue.songs.shift();
        module.exports.play(queue.songs[0], message);
      }

      console.error(error);
      return message.channel.send(`Erreur: ${error.message ? error.message : error}`);
    }

    queue.connection.on("disconnect", () => message.client.queue.delete(message.guild.id));

    const dispatcher = queue.connection
      .play(stream, { type: streamType })
      .on("finish", () => {
        if (collector && !collector.ended) collector.stop();

        if (queue.loop) {
          let lastSong = queue.songs.shift();
          queue.songs.push(lastSong);
          module.exports.play(queue.songs[0], message);
        } else {
          queue.songs.shift();
          module.exports.play(queue.songs[0], message);
        }
      })
      .on("error", (err) => {
        console.error(err);
        queue.songs.shift();
        module.exports.play(queue.songs[0], message);
      });
    dispatcher.setVolumeLogarithmic(queue.volume / 100);

    try {
      var playingMessage = await queue.textChannel.send(`üé∂ A commenc√© √† jouer: **${song.title}** ${song.url}`);
      await playingMessage.react("‚è≠");
      await playingMessage.react("‚èØ");
      await playingMessage.react("üîá");
      await playingMessage.react("üîâ");
      await playingMessage.react("üîä");
      await playingMessage.react("üîÅ");
      await playingMessage.react("‚èπ");
      await playingMessage.react("‚ù§Ô∏è");
    } catch (error) {
      console.error(error);
    }

    const filter = (reaction, user) => user.id !== message.client.user.id;
    var collector = playingMessage.createReactionCollector(filter, {
      time: song.duration > 0 ? song.duration * 1000 : 600000
    });

    collector.on("collect", (reaction, user) => {
      if (!queue) return;
      const member = message.guild.member(user);

      switch (reaction.emoji.name) {
        case "‚è≠":
          queue.playing = true;
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          queue.connection.dispatcher.end();
          queue.textChannel.send(`${user} ‚è© a skip la chanson`).catch(console.error);
          collector.stop();
          break;

        case "‚èØ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          if (queue.playing) {
            queue.playing = !queue.playing;
            queue.connection.dispatcher.pause(true);
            queue.textChannel.send(`${user} ‚è∏ a mis la musique en pause.`).catch(console.error);
          } else {
            queue.playing = !queue.playing;
            queue.connection.dispatcher.resume();
            queue.textChannel.send(`${user} ‚ñ∂ repris la musique!`).catch(console.error);
          }
          break;

        case "üîá":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          if (queue.volume <= 0) {
            queue.volume = 100;
            queue.connection.dispatcher.setVolumeLogarithmic(100 / 100);
            queue.textChannel.send(`${user} üîä unmute la musique!`).catch(console.error);
          } else {
            queue.volume = 0;
            queue.connection.dispatcher.setVolumeLogarithmic(0);
            queue.textChannel.send(`${user} üîá mute la musique!`).catch(console.error);
          }
          break;

        case "üîâ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          if (queue.volume - 10 <= 0) queue.volume = 0;
          else queue.volume = queue.volume - 10;
          queue.connection.dispatcher.setVolumeLogarithmic(queue.volume / 100);
          queue.textChannel
            .send(`${user} üîâ diminue le volume, le volume est maintenant ${queue.volume}%`)
            .catch(console.error);
          break;

        case "üîä":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          if (queue.volume + 10 >= 100) queue.volume = 100;
          else queue.volume = queue.volume + 10;
          queue.connection.dispatcher.setVolumeLogarithmic(queue.volume / 100);
          queue.textChannel
            .send(`${user} üîä augmente le volume, le volume est maintenant ${queue.volume}%`)
            .catch(console.error);
          break;

        case "üîÅ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          queue.loop = !queue.loop;
          queue.textChannel.send(`Loop est maintenant ${queue.loop ? "**on**" : "**off**"}`).catch(console.error);
          break;

        case "‚èπ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          queue.songs = [];
          queue.textChannel.send(`${user} ‚èπ arr√™te la musique!`).catch(console.error);
          try {
            queue.connection.dispatcher.end();
          } catch (error) {
            console.error(error);
            queue.connection.disconnect();
          }
          collector.stop();
          break;

        case "‚ù§Ô∏è":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          if (!db.get(`musiques_${user.id}`)) db.set(`musiques_${user.id}`, []);
          const dbUser = db.get(`musiques_${user.id}`);
          const verifArrayUser = dbUser.filter((musiques) => musiques.url === song.url);
          if (verifArrayUser.length === 0) {
            db.push(`musiques_${user.id}`, song);
            queue.textChannel.send(`${user}, **${song.title}** ‚ù§Ô∏è a √©t√© ajout√© √† vos favoris`).catch(console.error);
          } else {
            const newMusiquesArray = dbUser.filter((musiques) => musiques.url !== song.url);
            db.set(`musiques_${user.id}`, newMusiquesArray);
            queue.textChannel.send(`${user}, **${song.title}** üíî a √©t√© retir√© de vos favoris`).catch(console.error);
          }
          break;

        default:
          reaction.users.remove(user).catch(console.error);
          break;
      }
    });

    collector.on("end", () => {
      playingMessage.reactions.removeAll().catch(console.error);
      if (PRUNING && playingMessage && !playingMessage.deleted) {
        playingMessage.delete({ timeout: 3000 }).catch(console.error);
      }
    });
  }
};